from shop.models import Category, Product


def run():
    # 4 категории сумок
    categories = [
        "Сумки женские",
        "Сумки мужские",
        "Спортивные сумки",
        "Дорожные сумки"
    ]

    category_objects = []

    for name in categories:
        cat, _ = Category.objects.get_or_create(name=name)
        category_objects.append(cat)

    # 10 товаров сумок
    products = [
        ("Сумка кожаная классическая", "Стильная кожаная сумка для повседневного использования", 89.99),
        ("Сумка через плечо", "Удобная сумка на каждый день", 39.99),
        ("Спортивная сумка Nike", "Идеальна для спортзала и тренировок", 59.99),
        ("Дорожная сумка XL", "Большая вместительная сумка для путешествий", 120.00),
        ("Сумка мини", "Маленькая, но стильная сумочка", 24.99),
        ("Сумка‑тоут", "Удобная сумка для покупок и прогулок", 29.99),
        ("Сумка с цепочкой", "Модная сумка с металлической цепочкой", 49.99),
        ("Сумка бананка", "Популярная поясная сумка", 19.99),
        ("Сумка деловая", "Идеальна для документов и ноутбука", 74.99),
        ("Сумка спортивная Adidas", "Прочная сумка для тренировок", 54.99),
    ]

    # распределяем товары по категориям
    for i, (title, description, price) in enumerate(products):
        category = category_objects[i % 4]  # равномерно распределяем по 4 категориям

        Product.objects.get_or_create(
            title=title,
            defaults={
                "category": category,
                "description": description,
                "price": price
            }
        )

    print("Готово! Созданы 4 категории и 10 товаров.")
